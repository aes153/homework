def all_variants(text):
    n = len(text)  # Длина строки

    # Генерируем все возможные маски для подстрок (от 000 до 111...1)
    for mask in range(1 << n):  # Это 2^n вариантов
        result = ""  # Переменная для хранения текущей подпоследовательности

        # Проходим по каждому биту маски
        for bit in range(n):
            if mask & (1 << bit):  # Если бит установлен, добавляем соответствующий символ
                result += text[bit]

        # Возвращаем текущую подпоследовательность
        yield result


a = all_variants("abc")
for i in a:
    print(i)